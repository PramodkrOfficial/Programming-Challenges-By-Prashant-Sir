class SingleLine {
    public static void main(String[] args) {

        RightHalfPyramid();
        System.out.println();
        ReverseRightHalfPyramid();
        System.out.println();
        LeftHalfPyramid();
    }

    public static void RightHalfPyramid() {
        System.out.println(" *\n * *\n * * *\n * * * *\n * * * * * ");

    }
    public static void ReverseRightHalfPyramid() {
        System.out.println(" * * * * *\n * * * *\n * * *\n * *\n * ");

    }
    public static void LeftHalfPyramid() {
        System.out.println("           *\n         * *\n       * * *\n     * * * *\n   * * * * * ");

    }
}
